function slider_init(slider_container, slider_contents) {
    for (var i = 0; i < slider_contents.length; i++) {
        var slider_page = document.createElement("div");
        slider_page.className = "imgslider-page";
        slider_page.style.backgroundImage = "url(" + slider_contents[i].image + ")";

        var slider_text_container = document.createElement("div");
        slider_text_container.className = "imgslider-text-container";

        var slider_title = document.createElement("h1");
        slider_title.className = "imgslider-title";
        slider_title.innerHTML = slider_contents[i].title;

        var slider_author = document.createElement("p");
        slider_author.className = "imgslider-author";
        slider_author.innerHTML = slider_contents[i].author;

        slider_text_container.appendChild(slider_title);
        slider_text_container.appendChild(slider_author);
        slider_page.appendChild(slider_text_container);

        slider_container.appendChild(slider_page);
    }
}

function toons_cards_init(toons_cards_container, toons_cards_contents) {
    for (var i = 0; i < toons_cards_contents.length; i++) {
        var toons_card = document.createElement("div");
        toons_card.className = "toons-card";
        toons_card.style.backgroundImage = "url(" + toons_cards_contents[i].image + ")";

        var toons_card_info = document.createElement("div");
        toons_card_info.className = "toons-card-info";

        var toons_card_title = document.createElement("h3");
        toons_card_title.className = "toons-card-title";
        toons_card_title.innerHTML = toons_cards_contents[i].title;

        var toons_card_subscribers = document.createElement("p");
        toons_card_subscribers.className = "toons-card-subscribers";
        toons_card_subscribers.innerHTML = "<i class=\"fa-solid fa-heart\"></i> " + toons_cards_contents[i].subscriber;

        var toons_card_author = document.createElement("p");
        toons_card_author.className = "toons-card-author";
        toons_card_author.innerHTML = toons_cards_contents[i].author;

        var toons_card_genre = document.createElement("p");
        toons_card_genre.className = "toons-card-genre";
        toons_card_genre.innerHTML = toons_cards_contents[i].genre;

        toons_card_info.appendChild(toons_card_title);
        toons_card_info.appendChild(toons_card_subscribers);
        toons_card_info.appendChild(toons_card_author);
        toons_card_info.appendChild(toons_card_genre);

        var toons_card_description = document.createElement("div");
        toons_card_description.className = "toons-card-description";

        switch (toons_cards_contents[i].genre) {
            case "Action":
                bg_color = "#ff0000";
                break;
            case "Romance":
                bg_color = "#ff00ff";
                break;
            case "Comedy":
                bg_color = "#0000ff";
                break;
            case "Slice of Life":
                bg_color = "var(--color-primary)";
                break;
            case "Fantasy":
                bg_color = "#00ffff";
                break;
        }

        toons_card_description.style.backgroundColor = bg_color;

        var toons_card_title = document.createElement("p");
        toons_card_title.className = "toons-card-title";
        toons_card_title.innerHTML = toons_cards_contents[i].title;

        var toons_card_desc = document.createElement("p");
        toons_card_desc.innerHTML = toons_cards_contents[i].desc;

        toons_card_description.appendChild(toons_card_title);
        toons_card_description.appendChild(toons_card_desc);

        toons_card.appendChild(toons_card_info);
        toons_card.appendChild(toons_card_description);

        toons_cards_container.appendChild(toons_card);
    }
}

function tops_init(type, tops_container, tops_contents) {
    // tops_container.appendChild(document.createElement("hr"));
    for (var i = 0; i < tops_contents.length; i++) {
        var tops_card = document.createElement("a");
        tops_card.className = "tops-card";
        tops_card.href = "#";

        var tops_card_number = document.createElement("div");
        tops_card_number.className = "tops-card-number";

        var tops_card_number_p = document.createElement("p");
        tops_card_number_p.innerHTML = "#" + (i + 1);
        tops_card_number.appendChild(tops_card_number_p);

        var tops_card_thumbnail = document.createElement("div");
        tops_card_thumbnail.className = "tops-card-thumbnail";

        var tops_card_thumbnail_img = document.createElement("img");
        tops_card_thumbnail_img.src = tops_contents[i].image;
        tops_card_thumbnail_img.alt = tops_contents[i].title;
        tops_card_thumbnail.appendChild(tops_card_thumbnail_img);

        var tops_card_info = document.createElement("div");
        tops_card_info.className = "tops-card-info";

        var tops_card_genre = document.createElement("p");
        tops_card_genre.className = "tops-card-genre";
        tops_card_genre.innerHTML = tops_contents[i].genre;

        var tops_card_title = document.createElement("h3");
        tops_card_title.className = "tops-card-title";
        tops_card_title.innerHTML = tops_contents[i].title;

        var tops_card_subscribers = document.createElement("p");
        tops_card_subscribers.className = "tops-card-subscribers";
        if (type == "author") {
            text = "Followers";
        } else {
            text = "Subscribers";
        }
        tops_card_subscribers.innerHTML = "<i class=\"fa-solid fa-heart\"></i> " + tops_contents[i].subscriber + " " + text;

        var tops_card_author = document.createElement("p");
        tops_card_author.className = "tops-card-author";
        tops_card_author.innerHTML = tops_contents[i].author;

        if (type != "author") {
            tops_card_info.appendChild(tops_card_genre);
        }
        tops_card_info.appendChild(tops_card_title);
        tops_card_info.appendChild(tops_card_subscribers);

        if (type != "author") {
            tops_card_info.appendChild(tops_card_author);
        }
        tops_card.appendChild(tops_card_number);
        tops_card.appendChild(tops_card_thumbnail);
        tops_card.appendChild(tops_card_info);

        tops_container.appendChild(tops_card);
        // hr = document.createElement("hr");
        // tops_container.appendChild(hr);

    }
}

$(document).ready(function() {
    slider_init(document.getElementById("slider-container"), slider_contents);

    toons_cards_init(document.getElementById("toons-cards-popular"), toons_cards_popular_contents);

    toons_cards_init(document.getElementById("toons-cards-recommended"), toons_cards_recommended_contents);

    tops_init("author", document.getElementById("tops-author"), tops_author_contents);

    tops_init("mebtoons", document.getElementById("tops-mebtoon"), tops_mebtoons_contents);

    // slidesohow
    $('.imgslider-container').each(function() {
        var $this = $(this);
        var $imgslider = $this.find('.imgslider-page');
        var $imgslider_count = $imgslider.length;
        var $imgslider_current = 1;

        $imgslider.hide();
        $imgslider.eq($imgslider_current - 1).show();

        setInterval(function() {
            $imgslider.eq($imgslider_current - 1).fadeOut(500);
            if ($imgslider_current == $imgslider_count) {
                $imgslider_current = 0;
            }
            $imgslider.eq($imgslider_current).fadeIn(500);
            $imgslider_current++;
        }, 5000);
    });


});